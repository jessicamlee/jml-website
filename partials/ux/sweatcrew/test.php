        <h2>Test &amp; Iterate</h2>
        <div class="cs-subsection">
            <p>Although the design appeared practical initially&comma; I later learned I overlooked a problematic aspect &mdash; the navigation experience. Through a moderated usability study I planned and conducted&comma; I learned from real users&apos; feedback ways to improve the experience by making the interface more intuitive&period;</p>
            <p class="mb-sm">Before diving into the key findings of the study&comma; let&apos;s keep the goals of the study in mind&colon;</p>
            <ul class="mb-md">
                <li>Determine the useful and useless components</li>
                <li>Identify new&comma; overlooked pain points</li>
                <li>Create a list of areas to modify for an interruption&dash;free shopping experience</li>
            </ul>
            <p>After chatting with the participants&comma; here are the <span class="bolded">key findings</span> from the moderated usability study&colon;</p>
            <ol class="mb-md">
                <li><span class="bolded">The “How We Customize” section </span>confused users &lpar;or wasn&apos;t simple to navigate the website&rpar;&period;</li>
                <li><span class="bolded">“Dissolve” transitions </span> confused users about where the user is on the web page&period;</li>
                <li><span class="bolded">Reviewing the updated visuals </span> for the order would make it easier for users to follow along with the order process&period;</li>
            </ol>
