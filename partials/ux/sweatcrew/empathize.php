            <p><span class="bolded">Research Goals&colon;</span> While the ultimate goal is to create a usable&comma; responsive website for various screens&comma; the other priority is to make the experience <span class="bolded-italic">straightforward</span>&comma; <span class="bolded-italic">accessible</span>&comma; and <span class="bolded-italic">user-friendly</span>&period;</p>
